message1 = ""
print(message1.isalnum()) # It checks if the variable store something (char or digit) or not

message2 = "Nishant"
print(message2.isalpha()) # It checks if the variable store only characters (no digits) or not

message3 = "Nishant"
print(message3.isdigit()) # It checks if the variable store only digits (no characters) or not

message4 = "Nishant"
print(message4.islower()) # It checks if the variable store (atleast one or more) only lowerase characters or not

message5 = "NISHANT"
print(message5.isupper()) # It checks if the variable store (atleast one or more) only uppercase characters or not

message6 = "  "
print(message6.isspace()) # It checks if the variable store only whitespace characters or not